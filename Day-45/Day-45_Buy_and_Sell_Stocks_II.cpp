class Solution {
  public:
    int stockBuyAndSell(int n, vector<int> &arr) {
        // code here
        int sum=0;
        int low=0,high=0;
        for(int i=0; i<n; i++)
        {
            if(arr[high]< arr[i])
                high++;
            else
            {
                sum+= arr[high]-arr[low];
                low=i;
                high=i;
            }
        }
        sum+= arr[high]-arr[low];
        return sum;
        
    }
};


